package com.pbd.IndividualDigits;

public class IndividualDigits {
    public static void main(String [] args) {
        for (int e = 1; e < 10; e++) {
            for (int h = 0; h < 10; h++) {
                System.out.println(e + "" + h + ", " + e + " + " + h + " = " + (e + h));
            }
        }
    }
}
