package com.pbd.Basic_ArrayLists_1;

import java.util.*;

public class BasicArrayLists {
    public static void main(String[] args) {
        int n = 10;
        ArrayList<Integer> numbers = new ArrayList<Integer>(n);

        for (int i = 0; i < n; i++) {
            numbers.add(-113);
        }
        System.out.println(numbers);
    }
}
